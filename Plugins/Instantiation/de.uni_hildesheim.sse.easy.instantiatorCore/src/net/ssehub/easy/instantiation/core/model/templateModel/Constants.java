package net.ssehub.easy.instantiation.core.model.templateModel;

/**
 * General language constants.
 * 
 * @author Holger Eichelberger
 */
public class Constants {

    /**
     * The name for the indentation level entry in an indentation hint.
     */
    public static final String INDENTATION_HINT_INDENTATION = "indentation";

    /**
     * The name for the tab emulation entry in an indentation hint.
     */
    public static final String INDENTATION_HINT_TAB_EMU = "tabEmulation";

    /**
     * The name for the additional indentation entry in an indentation hint.
     */
    public static final String INDENTATION_HINT_ADDITIONAL = "additional";
    
    /**
     * The name for the optional formatting hint to define the line end.
     */
    public static final String FORMATTING_HINT_LINEEND = "lineEnd";

    /**
     * The name for the optional formatting hint to define the (maximum) line length.
     */
    public static final String FORMATTING_HINT_LINELENGTH = "lineLength";

    /**
     * The name for the optional formatting hint to define a single indentation step in terms of the number 
     * of whitespaces.
     */
    public static final String FORMATTING_HINT_INDENTSTEPS = "indentSteps";

    /**
     * The name for the optional formatting hint to define whether tabs shall be used.
     */
    public static final String FORMATTING_HINT_USETABS = "useTabs";
    
    /**
     * The name for the optional formatting hint to define the profile.
     */
    public static final String FORMATTING_HINT_PROFILE = "profile";

    /**
     * The prefix for the optional formatting hint to define a profile argument.
     */
    public static final String FORMATTING_HINT_PROFILE_ARG_PREFIX = "profileArg_";
    
}
